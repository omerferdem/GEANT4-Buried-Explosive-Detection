B1PrimaryGeneratorAction::B1PrimaryGeneratorAction()
: G4VUserPrimaryGeneratorAction(),
  fParticleGun(0), 
  fEnvelopeBox(0)
{
  G4int n_particle = 1;
  fParticleGun  = new G4ParticleGun(n_particle);

  // default particle kinematic
  G4ParticleTable* particleTable = G4ParticleTable::GetParticleTable();
  G4String particleName;
  G4ParticleDefinition* particle
    = particleTable->FindParticle(particleName="neutron");
  fParticleGun->SetParticleDefinition(particle);
  fParticleGun->SetParticleMomentumDirection(G4ThreeVector(1.,0.,0.));
  fParticleGun->SetParticleEnergy(0.02*eV);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

B1PrimaryGeneratorAction::~B1PrimaryGeneratorAction()
{
  delete fParticleGun;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B1PrimaryGeneratorAction::GeneratePrimaries(G4Event* anEvent)
{
  //this function is called at the begining of ecah event
  //

  // In order to avoid dependence of PrimaryGeneratorAction
  // on DetectorConstruction class we get Envelope volume
  // from G4LogicalVolumeStore.

  G4double thickness_param = 0.014 * mm;

  G4double COUNT_X = 1;
  G4double COUNT_Y = 1;
  G4double COUNT_Z = 1;

  G4double sens_hx = 0.1 * mm;
  G4double shell_hx = thickness_param + sens_hx * mm;
  G4double shell_hy = thickness_param + 1.5 * mm;
  G4double shell_hz = thickness_param + 1.25 * mm;

  //G4LogicalVolume* shellLog = G4LogicalVolumeStore::GetInstance()->GetVolume("Shell");
  //G4Box* fShellBox = dynamic_cast<G4Box*>(shellLog->GetSolid());

  //shell_hy = fEnvelopeBox->GetYHalfLength() * 2.;
  //shell_hz = fEnvelopeBox->GetZHalfLength() * 2.;

  G4double x0 = -0.*mm;
  G4double y0 = 2 * COUNT_Y * shell_hy * G4UniformRand();
  G4double z0 = 2 * COUNT_Z * shell_hz * G4UniformRand();

  fParticleGun->SetParticlePosition(G4ThreeVector(x0, y0, z0));

  fParticleGun->GeneratePrimaryVertex(anEvent);
  }

